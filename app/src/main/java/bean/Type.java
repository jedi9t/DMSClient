package bean;

public class Type {
int id;
String name;
String htmlContent;

public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getHtmlContent() {
	return htmlContent;
}
public void setHtmlContent(String htmlContent) {
	this.htmlContent = htmlContent;
}


}
